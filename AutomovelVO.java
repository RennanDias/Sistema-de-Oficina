package VO;

public class AutomovelVO {
	private String marca;
	private String modelo;
	private String cor;
	private String placa;
	private int ano;
	private int quilometragem;
	private PeçaVO peças[];
	private ClientesVO dono;
	private OrçamentoVO orçamento[];
	private Long id;
	
	public String getMarca() {
		return marca;
	}
	
	public void setMarca(String marca) {
		if (marca != null) {
			if (marca.length() == 0) {
				System.out.println("Entrada inválida! Digite algo.");
			}
			else {
				this.marca = marca;
			}
		}
		else {
			System.out.println("Entrada inválida! Digite algo.");
		}
	}
	
	public String getModelo() {
		return modelo;
	}
	
	public void setModelo(String modelo) {
		if (modelo != null) {
			if (modelo.length() == 0) {
				System.out.println("Entrada inválida! Digite algo.");
			}
			else {
				this.modelo = modelo;
			}
		}
		else {
			System.out.println("Entrada inválida! Digite algo.");
		}
	}
	
	public String getCor() {
		return cor;
	}
	
	public void setCor(String cor) {
		if (cor != null) {
			if (cor.length() == 0) {
				System.out.println("Entrada inválida! Digite algo.");
			}
			else {
				this.cor = cor;
			}
		}
		else {
			System.out.println("Entrada inválida! Digite algo.");
		}
	}
	
	public String getPlaca() {
		return placa;
	}
	
	public void setPlaca(String placa) {
		if (placa != null) {
			if(placa.length() != 7 || !placa.substring(0, 3).matches("[A-Z]*") || !placa.substring(3, 4).matches("[0-9]*") ||
					  !placa.substring(4, 5).matches("[A-Z]*") || !placa.substring(5, 7).matches("[0-9]*")) {
						System.out.println("Placa Inválida, tente o modelo XXX0X00");
					}
					else {
						this.placa = placa;
					}
		}
		else {
			System.out.println("Entrada inválida! Digite algo.");
		}
	}
	
	
	public int getAno() {
		return ano;
	}
	
	public void setAno(int ano) {
		if (ano < 1886) {
			System.out.println("Entrada inválida! O primeiro automóvel foi criado somente em 1886!");
		}
		else {
			this.ano = ano;
		}
	}
	
	public int getQuilometragem() {
		return quilometragem;
	}
	
	public void setQuilometragem(int quilometragem) {
		if (quilometragem < 0) {
			System.out.println("Entrada inválida! Digite um valor positivo!");
		}
		else {
			this.quilometragem = quilometragem;
		}
	}
	
	public PeçaVO[] getPeças() {
		return peças;
	}
	
	public void setPeças(PeçaVO[] peças) {
		if (peças != null) {
			this.peças = peças;
		}
		else {
			System.out.println("Nada encontrado! Adicione peças para este automóvel!");
		}
	}
	
	public ClientesVO getDono() {
		return dono;
	}
	
	public void setDono(ClientesVO dono) {
		this.dono = dono;
	}
	
	public OrçamentoVO[] getOrçamento() {
		return orçamento;
	}
	
	public void setOrçamento(OrçamentoVO[] orçamento) {
		if (orçamento != null) {
			this.orçamento = orçamento;
		}
		else {
			System.out.println("Nada encontrado! Adicione um orçamento para este automóvel!");
		}
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	
}
